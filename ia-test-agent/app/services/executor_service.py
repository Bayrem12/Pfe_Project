    async def _execute_script(
        self, page: Page, script_code: str, test_id: str,
        already_navigated_url: str = "",
    ) -> tuple[str, list[StepResult], list[str]]:
        """Execute embedded Playwright commands from generated script.

        Args:
            already_navigated_url: If set, the FIRST `await page.goto(URL)` line
                whose URL matches this value is silently skipped — prevents the
                Gherkin `Given I navigate to URL` step from re-navigating after
                `execute()` already used `request.url_cible`. The skipped step
                still appears in results as OK so the user sees their step ran.
        """
        results: list[StepResult] = []
        screenshots: list[str] = []

        # Extract executable lines from the script (lines with 'await page.' or 'await expect').
        # Skip lines that are inside helper function definitions — those contain variables
        # like `path`, `frame_name` that are undefined at the top level and would cause
        # spurious FAILED steps.
        lines = script_code.splitlines()
        exec_blocks: list[tuple[str, str]] = []  # (comment, code_line)
        current_comment = ""
        inside_helper_func = False  # True while inside a def _xxx() helper body

        for line in lines:
            stripped = line.strip()

            # Detect entry into a helper function definition (starts at column 0, name starts with _)
            if re.match(r"^async def _\w+\(|^def _\w+\(", line):
                inside_helper_func = True
                continue
            # Detect entry into the main test function — stop skipping
            if re.match(r"^async def test_\w+\(|^async def \w+\(", line):
                inside_helper_func = False
                continue
            # A non-indented non-empty, non-comment line closes any helper scope
            if line and not line[0].isspace() and not stripped.startswith("#"):
                inside_helper_func = False

            if inside_helper_func:
                continue  # skip everything inside helper function bodies

            if stripped.startswith("#") and re.search(r"[Ss]tep\s+\d+", stripped):
                # Handles both "# Step N:" and "# ── Step N:" (template uses em-dashes)
                current_comment = re.sub(r"^#\s*\u2500*\s*\u2014*\s*\u2212*\s*[-─]*\s*", "", stripped)
                current_comment = re.sub(r"\s*[-─\u2500]+\s*$", "", current_comment).strip()
            elif (
                ("await page." in stripped or "await expect" in stripped
                 or stripped.startswith("assert "))
                and not stripped.startswith("#")  # skip commented-out placeholder lines
            ):
                # Only collect lines that belong to a named step (after a # ── Step N: comment).
                # Lines before the first step comment are part of the generated script's
                # preamble/initial-navigation block — the executor already handled navigation
                # via url_cible, so those would cause duplicates in the results.
                # `assert ...` lines are emitted by strict-assertion generation
                # (dropdown selected-value, login success URL check, visible-text match).
                if current_comment:
                    exec_blocks.append((current_comment, stripped))
            elif stripped.startswith("loc = ") or stripped.startswith("locator = "):
                exec_blocks.append((current_comment or stripped, stripped))

        step_num = 0
        # Pre-compile a regex to extract the URL string from `await page.goto("...")`
        _goto_url_re = re.compile(r'await\s+page\.goto\(\s*["\']([^"\']+)["\']')

        # ── Group consecutive lines by their step comment so a single Gherkin
        # step that emits multiple await lines (e.g. `naviguer` emits goto +
        # wait_for_load_state) shows up as ONE StepResult, not N. ──────────
        grouped: list[tuple[str, list[str]]] = []
        for c, code in exec_blocks:
            if grouped and grouped[-1][0] == c:
                grouped[-1][1].append(code)
            else:
                grouped.append((c, [code]))

        skipped_initial_goto = False
        for comment, codes in grouped:
            step_num += 1
            step_start = time.time()

            # ── Skip duplicate navigate ──────────────────────────────────
            # If `execute()` already navigated to `already_navigated_url`,
            # and the FIRST step's first line is `page.goto(SAME_URL)`,
            # report the whole step as OK without re-running it. Avoids
            # the "Step 1 appears twice" symptom in the report.
            if (
                not skipped_initial_goto
                and already_navigated_url
                and codes
                and "page.goto(" in codes[0]
            ):
                m = _goto_url_re.search(codes[0])
                if m and m.group(1).rstrip("/") == already_navigated_url.rstrip("/"):
                    skipped_initial_goto = True
                    elapsed = round((time.time() - step_start) * 1000, 1)
                    results.append(StepResult(
                        step=comment, statut="OK", duree_ms=elapsed,
                    ))
                    if settings.STEP_SCREENSHOT:
                        ss = await self._screenshot(page, test_id, f"step_{step_num}")
                        if ss:
                            screenshots.append(ss)
                    continue

            # ── Execute every line in this step; first failure stops the group
            total_retry = 0
            total_visual = False
            total_adapt = ""
            step_trace: Optional[FallbackTrace] = None
            step_pw_err = ""
            failure: Exception | None = None
            failure_is_step_error = False

            for code in codes:
                try:
                    r, v, a, tr, pw = await self._exec_with_retry(page, code)
                    total_retry += r
                    total_visual = total_visual or v
                    total_adapt = total_adapt or a
                    if tr is not None:
                        step_trace = tr
                    if pw:
                        step_pw_err = pw
                except _StepFailedError as sfe:
                    failure = sfe
                    failure_is_step_error = True
                    total_retry = max(total_retry, sfe.retry_count)
                    total_visual = total_visual or sfe.fallback_tried
                    if sfe.fallback_trace is not None:
                        step_trace = sfe.fallback_trace
                    if sfe.playwright_error:
                        step_pw_err = sfe.playwright_error
                    break
                except Exception as e:
                    failure = e
                    break

            elapsed = round((time.time() - step_start) * 1000, 1)
            if failure is None:
                results.append(StepResult(
                    step=comment, statut="OK", duree_ms=elapsed,
                    retry_count=total_retry, visual_fallback_used=total_visual,
                    adaptation_appliquee=total_adapt,
                    fallback_trace=step_trace,
                    playwright_error=step_pw_err,
                ))
            elif failure_is_step_error:
                sfe = failure  # type: ignore[assignment]
                results.append(StepResult(
                    step=comment, statut="FAILED", duree_ms=elapsed,
                    erreur=str(sfe.original),
                    retry_count=sfe.retry_count,
                    visual_fallback_used=sfe.fallback_tried,
                    fallback_trace=sfe.fallback_trace or step_trace,
                    playwright_error=sfe.playwright_error or step_pw_err,
                ))
            else:
                results.append(StepResult(
                    step=comment, statut="FAILED", duree_ms=elapsed,
                    erreur=str(failure),
                ))

            # Screenshot once per step (not once per await line)
            if settings.STEP_SCREENSHOT:
                ss = await self._screenshot(page, test_id, f"step_{step_num}")
                if ss:
                    screenshots.append(ss)

        overall = "PASSED" if all(r.statut == "OK" for r in results) else "FAILED"
        return overall, results, screenshots

    async def _exec_with_retry(
        self, page: Page, line: str
    ) -> tuple[int, bool, str, Optional[FallbackTrace], str]:
        """
        Execute une ligne Playwright avec retries + AdaptationService (Day 14/15).

        Retourne (retry_count, visual_fallback_used, adaptation_appliquee,
                  fallback_trace, playwright_error).
        """
        last_exc: Exception = Exception("No attempt made")
        visual_used = False
        fallback_tried = False
        adaptation_str = ""
        last_trace: Optional[FallbackTrace] = None
        last_pw_error = ""

        for attempt in range(1, settings.MAX_RETRIES + 2):  # 1 + up to 2 retries
            if page.is_closed():
                last_exc = RuntimeError("Playwright page is closed — aborting step")
                last_pw_error = "PageClosed: page was closed before attempt"
                break
            try:
                visual_success, attempted, trace, pw_err = (
                    await self._exec_line_with_fallback_tracked(page, line)
                )
                fallback_tried = fallback_tried or attempted
                visual_used = visual_used or visual_success
                if trace is not None:
                    last_trace = trace
                if pw_err:
                    last_pw_error = pw_err
                return attempt - 1, visual_used, adaptation_str, last_trace, last_pw_error
            except _StepFailedError as sfe:
                fallback_tried = True
                last_exc = sfe.original
                if sfe.fallback_trace is not None:
                    last_trace = sfe.fallback_trace
                if sfe.playwright_error:
                    last_pw_error = sfe.playwright_error
                e = sfe.original
                # Assertions (expect/assert lines) must NOT be adapted away —
                # that would turn a real failure into a false PASS.
                stripped = line.strip()
                is_assertion_line = (
                    stripped.startswith("await expect(")
                    or stripped.startswith("expect(")
                    or stripped.startswith("assert ")
                )
                if is_assertion_line:
                    break  # propagate as failure immediately, no retry
                if attempt <= settings.MAX_RETRIES:
                    delay_ms = 500 * (2 ** (attempt - 1))
                    logger.warning(
                        "Step attempt %d/%d failed (fallback tried, %s) — getting adaptation strategy…",
                        attempt, settings.MAX_RETRIES + 1, e,
                    )
                    if page.is_closed():
                        break
                    adaptation_str = await self._run_adaptation(page, e, line, adaptation_str)
                    if not page.is_closed():
                        await page.wait_for_timeout(delay_ms)
                        await self._handle_unexpected_modal(page)
            except Exception as e:
                last_exc = e
                if attempt <= settings.MAX_RETRIES:
                    delay_ms = 500 * (2 ** (attempt - 1))
                    logger.warning(
                        "Step attempt %d/%d failed (%s) — getting adaptation strategy…",
                        attempt, settings.MAX_RETRIES + 1, e,
                    )

                    # ── AdaptationService diagnosis ──────────────────────────
                    try:
                        from app.schemas.test_schemas import AdaptationRequest  # noqa: PLC0415
                        screenshot_b64 = ""
                        try:
                            ss_bytes = await page.screenshot()
                            import base64  # noqa: PLC0415
                            screenshot_b64 = base64.b64encode(ss_bytes).decode()
                        except Exception:
                            pass

                        adapt_req = AdaptationRequest(
                            type_erreur=type(e).__name__ + ": " + str(e)[:200],
                            step_en_cours={"line": line},
                            screenshot_actuel=screenshot_b64,
                        )
                        adapt_resp = self._get_adaptation().adapt(adapt_req)
                        adaptation_str = adapt_resp.strategie or adaptation_str
                        logger.info(
                            "Adaptation strategy: %s (conf=%.2f)",
                            adapt_resp.strategie, adapt_resp.confiance,
                        )

                        for action in adapt_resp.actions_correctives:
                            if action.action == "press_escape":
                                await self._handle_unexpected_modal(page)
                                break
                            elif action.action in ("click", "cookie_close"):
                                await self._handle_cookie_popup(page)
                                break
                            elif action.action == "screenshot_and_redetect":
                                pass
                    except Exception as adapt_err:
                        logger.debug("AdaptationService error: %s", adapt_err)

                    await page.wait_for_timeout(delay_ms)
                    await self._handle_unexpected_modal(page)

        raise _StepFailedError(
            last_exc,
            retry_count=settings.MAX_RETRIES,
            fallback_tried=fallback_tried,
            fallback_trace=last_trace,
            playwright_error=last_pw_error,
        )

    async def _run_adaptation(self, page: Page, exc: Exception, line: str, adaptation_str: str) -> str:
        """Run AdaptationService and apply first corrective action. Returns updated strategy str."""
        try:
            from app.schemas.test_schemas import AdaptationRequest  # noqa: PLC0415
            screenshot_b64 = ""
            try:
                ss_bytes = await page.screenshot()
                import base64  # noqa: PLC0415
                screenshot_b64 = base64.b64encode(ss_bytes).decode()
            except Exception:
                pass

            adapt_req = AdaptationRequest(
                type_erreur=type(exc).__name__ + ": " + str(exc)[:200],
                step_en_cours={"line": line},
                screenshot_actuel=screenshot_b64,
            )
            adapt_resp = self._get_adaptation().adapt(adapt_req)
            new_strategy = adapt_resp.strategie or adaptation_str
            logger.info("Adaptation strategy: %s (conf=%.2f)", adapt_resp.strategie, adapt_resp.confiance)

            for action in adapt_resp.actions_correctives:
                if action.action == "press_escape":
                    await self._handle_unexpected_modal(page)
                    break
                elif action.action in ("click", "cookie_close"):
                    await self._handle_cookie_popup(page)
                    break
            return new_strategy
        except Exception as adapt_err:
            logger.debug("AdaptationService error: %s", adapt_err)
            return adaptation_str

