class _StepFailedError(Exception):
    """
    Raised when a step fails after all retries.

    Carries retry metadata so the caller can record accurate stats
    even for ultimately-failed steps:
      - retry_count     : number of retry attempts made (0 = only 1 attempt)
      - fallback_tried  : True if visual fallback was attempted at least once
      - fallback_trace  : FallbackTrace from the *last* fallback attempt (if any)
      - playwright_error: the original Playwright locator error message
    """
    def __init__(
        self,
        original: Exception,
        retry_count: int,
        fallback_tried: bool,
        fallback_trace: Optional["FallbackTrace"] = None,
        playwright_error: str = "",
    ):
        self.original = original
        self.retry_count = retry_count
        self.fallback_tried = fallback_tried
        self.fallback_trace = fallback_trace
        self.playwright_error = playwright_error or str(original)
        super().__init__(str(original))


