    async def _exec_line_with_fallback_tracked(
        self, page: Page, line: str
    ) -> tuple[bool, bool, Optional[FallbackTrace], str]:
        """
        Execute 'line' with visual-fallback safety net.

        Returns
        -------
        (visual_success, fallback_attempted, fallback_trace, playwright_error)
            visual_success    – True if visual fallback was used AND succeeded
            fallback_attempted – True if visual fallback was tried (even if it failed)
            fallback_trace    – diagnostic data from the fallback attempt (or None)
            playwright_error  – original locator error message (or "")
        """
        try:
            await self._exec_line(page, line)
            return False, False, None, ""
        except Exception as original_error:
            target = _parse_target_from_line(line)
            pw_error = f"{type(original_error).__name__}: {str(original_error)[:300]}"
            # Visual fallback can ONLY recover interactive actions
            # (click / fill / hover / check / select). It is structurally
            # meaningless for assertions like `expect(...).to_be_visible()`
            # or `to_contain_text(...)` — clicking pixels cannot make a
            # missing text appear. Skip the (expensive) YOLO+DOM scan in
            # that case and let the original error propagate verbatim.
            stripped = line.strip()
            is_assertion = (
                stripped.startswith("await expect(")
                or stripped.startswith("expect(")
                or stripped.startswith("assert ")
            )
            if is_assertion:
                raise _StepFailedError(
                    original_error, retry_count=0, fallback_tried=False,
                    fallback_trace=None, playwright_error=pw_error,
                )
            # None  → no locator in this line at all (e.g. wait_for_load_state) → skip
            # ""    → locator found but no text label → type-only fallback
            # "foo" → text label extracted → text + type fallback
            if target is not None:
                trace = await self._visual_fallback(page, line, target)
                if trace.success:
                    logger.info("Visual fallback succeeded for: %s", line.strip())
                    return True, True, trace, pw_error
                # Fallback was attempted but couldn't recover
                raise _StepFailedError(
                    original_error, retry_count=0, fallback_tried=True,
                    fallback_trace=trace, playwright_error=pw_error,
                )
            raise original_error

    async def _exec_line_with_fallback(self, page: Page, line: str):
        """Execute a Playwright line; fall back to visual detection on locator failure."""
        await self._exec_line_with_fallback_tracked(page, line)

    async def _visual_fallback(self, page: Page, line: str, target: str) -> FallbackTrace:
        """
        Fallback visuel : screenshot → YOLO+DOM detection → click/fill aux coordonnees.

        Returns a fully-populated FallbackTrace describing what was detected,
        which candidate won (or none), and whether the action succeeded.
        Saves both raw and annotated screenshots for the diagnostic report.
        """
        type_filter = _intended_types_from_line(line)
        trace = FallbackTrace(target_text=target, type_filter=type_filter)

        try:
            screenshot_bytes = await page.screenshot()
            vision = self._get_vision()
            detection, yolo_raw, dom_raw = await vision.detect_from_page_with_breakdown(
                page, screenshot_bytes
            )

            trace.yolo_count = len(yolo_raw)
            trace.dom_count = len(dom_raw)
            trace.merged_count = len(detection.elements)

            scored = _score_candidates(detection.elements, target, type_filter)
            # Top-15 candidates are enough for a UI table; full list is rarely useful
            top_n = scored[:15]
            for rank, (elem, score) in enumerate(top_n, 1):
                bb = elem.bounding_box
                trace.candidates.append(DetectionCandidate(
                    rank=rank,
                    type=elem.type,
                    label=(elem.label or "")[:120],
                    source=elem.source,
                    confiance_detection=float(elem.confiance_detection),
                    similarity_score=round(float(score), 3),
                    bbox_x=bb.x, bbox_y=bb.y, bbox_w=bb.width, bbox_h=bb.height,
                    texte_ocr=(elem.texte_ocr or "")[:120],
                    was_chosen=False,
                ))

            elem = _find_best_element(detection.elements, target, type_filter)

            # Persist screenshots regardless of outcome — they're invaluable
            # for diagnosing *why* fallback failed.
            test_id = getattr(self, "_current_test_id", "step")
            step_idx = getattr(self, "_current_step_idx", 0)
            self._current_step_idx = step_idx + 1  # auto-increment per fallback
            raw_path = ""
            ann_path = ""
            try:
                os.makedirs(settings.SCREENSHOTS_DIR, exist_ok=True)
                raw_path = os.path.join(
                    settings.SCREENSHOTS_DIR,
                    f"{test_id}_fb{step_idx}_raw.png",
                )
                with open(raw_path, "wb") as f:
                    f.write(screenshot_bytes)
                from app.services.vision_service import VisionService  # noqa: PLC0415
                annotated_bytes = VisionService.annotate_screenshot(
                    screenshot_bytes, yolo_raw, dom_raw, winner=elem
                )
                ann_path = os.path.join(
                    settings.SCREENSHOTS_DIR,
                    f"{test_id}_fb{step_idx}_annotated.png",
                )
                with open(ann_path, "wb") as f:
                    f.write(annotated_bytes)
            except Exception as ss_err:
                logger.debug("Failed to save fallback screenshots: %s", ss_err)
            trace.screenshot_raw = raw_path
            trace.screenshot_annotated = ann_path

            if elem is None:
                logger.debug("Visual fallback: no matching element for '%s'", target)
                trace.success = False
                return trace

            # Mark winner in candidates list
            for c in trace.candidates:
                if (
                    c.bbox_x == elem.bounding_box.x
                    and c.bbox_y == elem.bounding_box.y
                    and c.type == elem.type
                ):
                    c.was_chosen = True
                    trace.winner_rank = c.rank
                    trace.winner_source = c.source
                    break

            bb = elem.bounding_box
            cx = bb.x + bb.width // 2
            cy = bb.y + bb.height // 2
            trace.winner_coords_x = cx
            trace.winner_coords_y = cy

            # Dispatch the correct mouse/keyboard action
            if ".fill(" in line:
                trace.action_dispatched = "fill"
                m = re.search(r'\.fill\("([^"]*)"\)', line)
                value = m.group(1) if m else ""
                # Primary: JS direct-set — more reliable than keyboard after a locator timeout
                try:
                    filled = await page.evaluate(
                        """([cx, cy, val]) => {
                            const el = document.elementFromPoint(cx, cy);
                            if (el && (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA')) {
                                el.focus();
                                el.value = val;
                                el.dispatchEvent(new InputEvent('input', {bubbles: true, data: val}));
                                el.dispatchEvent(new Event('change', {bubbles: true}));
                                return true;
                            }
                            return false;
                        }""",
                        [cx, cy, value],
                    )
                except Exception:
                    filled = False
                if not filled:
                    await page.mouse.click(cx, cy)
                    await page.wait_for_timeout(150)
                    await page.keyboard.press("Control+a")
                    await page.keyboard.type(value, delay=10)

            elif ".clear()" in line:
                trace.action_dispatched = "clear"
                await page.mouse.click(cx, cy)
                await page.keyboard.press("Control+a")
                await page.keyboard.press("Delete")

            elif ".check()" in line or ".uncheck()" in line:
                trace.action_dispatched = "check" if ".check()" in line else "uncheck"
                want_checked = ".check()" in line
                already_correct = await page.evaluate(
                    """([cx, cy, want]) => {
                        const el = document.elementFromPoint(cx, cy);
                        if (el && el.tagName === 'INPUT' &&
                            (el.type === 'checkbox' || el.type === 'radio')) {
                            return el.checked === want;
                        }
                        return false;
                    }""",
                    [cx, cy, want_checked],
                )
                if not already_correct:
                    await page.mouse.click(cx, cy)

            elif ".select_option(" in line:
                trace.action_dispatched = "select_option"
                m = re.search(r'\.select_option\((?:label=)?"([^"]*)"\)', line)
                value = m.group(1) if m else ""
                await page.mouse.click(cx, cy)
                await page.wait_for_timeout(300)
                if value:
                    await page.keyboard.type(value)
                    await page.keyboard.press("Enter")

            elif ".hover()" in line:
                trace.action_dispatched = "hover"
                await page.mouse.move(cx, cy)

            elif ".click" in line:
                trace.action_dispatched = "click"
                await page.mouse.click(cx, cy)
                await page.wait_for_load_state("networkidle", timeout=5000)

            else:
                trace.success = False
                return trace

            logger.info(
                "Visual fallback: '%s' → %s(%s) at (%d, %d) conf=%.2f src=%s",
                target, elem.type, elem.texte_ocr or elem.label,
                cx, cy, elem.confiance_detection, elem.source,
            )
            trace.success = True
            return trace

        except Exception as e:
            logger.debug("Visual fallback error: %s", e)
            trace.success = False
            return trace

    async def _exec_line(self, page: Page, line: str):
        """Execute a single Playwright line dynamically."""
        line = line.strip()
        if not line or line.startswith("#"):
            return

        # Build a safe local namespace with page and expect
        local_ns = {"page": page, "expect": expect, "re": __import__("re")}
        # Wrap in an async function for await support
        code = f"async def __exec(_page, _expect, _re):\n"
        code += f"    page = _page\n"
        code += f"    expect_fn = _expect\n"

        # Replace expect( with expect_fn( to avoid shadowing
        exec_line = line.replace("expect(", "expect_fn(")
        code += f"    {exec_line}\n"

        compiled = compile(code, "<playwright_step>", "exec")
        exec(compiled, local_ns)
        await local_ns["__exec"](page, expect, __import__("re"))

    async def _screenshot(self, page: Page, test_id: str, label: str) -> str:
        """Capture un screenshot et retourne le chemin."""
        try:
            filename = f"{test_id}_{label}.png"
            filepath = os.path.join(settings.SCREENSHOTS_DIR, filename)
            await page.screenshot(path=filepath, full_page=False)
            return filepath
        except Exception:
            return ""
