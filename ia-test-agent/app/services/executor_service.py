def _parse_target_from_line(line: str) -> Optional[str]:
    """Extrait le texte cible d'une ligne Playwright pour le fallback visuel.

    Returns:
        str  — text label extracted (can be empty string for type-only fallback)
        None — line has no actionable locator (e.g. wait_for_load_state)
    """
    patterns = [
        r'get_by_label\("([^"]+)"\)',
        r'get_by_placeholder\("([^"]+)"\)',
        r'get_by_text\("([^"]+)"\)',
        r'get_by_role\([^,]+,\s*name="([^"]+)"\)',
        r'filter\(has_text="([^"]+)"\)',
    ]
    for pat in patterns:
        m = re.search(pat, line)
        if m:
            return m.group(1)
    # locator("css") — extract meaningful CSS target; "body" is a no-op placeholder.
    # The regex handles both single-quoted and escaped-double-quoted attribute values
    # e.g.  locator("select[id='foo']")
    #        locator("select[id=\"foo\"]")
    m = re.search(r'locator\("((?:[^"\\]|\\.)*)"', line)
    if m:
        sel = m.group(1)
        if sel in ("body",):
            return None
        # For compound CSS attribute selectors generated by the generator
        # (e.g. "input[name='Email' i], input[id='Email' i]..."),
        # extract the first attribute *value* as the meaningful text label so
        # _find_best_element can score against 'Email' instead of '' — which
        # would cause it to blindly pick the first input_text on the page.
        attr_val_m = re.search(
            r"\[(?:name|id|placeholder|aria-label)=['\"]([^'\"]+)['\"]",
            sel,
        )
        if attr_val_m:
            return attr_val_m.group(1)  # e.g. "Email"
        # For selectors like input[type='email'], extract the type value as a
        # semantic hint: DOM enrichment often stores 'userEmail name@example.com'
        # which contains 'email' — enough for similarity matching to win.
        type_val_m = re.search(r"\[type=['\"]([^'\"]+)['\"]", sel)
        if type_val_m:
            return type_val_m.group(1)  # e.g. 'email', 'password', 'text'
        # CSS selector present but no extractable label → type-only fallback
        return ""
    return None


def _intended_types_from_line(line: str) -> list[str]:
    """Determine les types YOLO pertinents pour une action donnee."""
    if ".fill(" in line or ".clear()" in line:
        return ["input_text", "input_password"]
    if ".check()" in line or ".uncheck()" in line:
        return ["checkbox"]
    if ".select_option(" in line:
        return ["dropdown"]
    if ".click()" in line or ".click(" in line or ".hover()" in line:
        return ["button", "link", "checkbox", "dropdown"]
    return []


def _score_candidates(elements, target: str, type_filter: list[str]):
    """
    Score every detected element against `target` for visual fallback.

    Returns
    -------
    list[tuple[DetectedElement, float]]   sorted by score DESC
        Every input element is included so the caller (FallbackTrace)
        can show all candidates considered.
    """
    target_lower = target.lower()

    candidates = [e for e in elements if not type_filter or e.type in type_filter]
    if not candidates:
        candidates = list(elements)

    scored: list[tuple] = []
    for elem in candidates:
        text = (elem.texte_ocr or elem.label or "").lower()
        if not text:
            score = 0.1  # type-only base score
        else:
            score = difflib.SequenceMatcher(None, target_lower, text).ratio()
            if target_lower and target_lower in text:
                score += 0.3  # substring bonus
        scored.append((elem, score))

    # Sort by score DESC; break ties using YOLO confidence DESC so that the
    # highest-confidence detection of a given type wins when scores are equal.
    scored.sort(key=lambda t: (t[1], t[0].confiance_detection), reverse=True)
    return scored


def _find_best_element(elements, target: str, type_filter: list[str]):
    """Trouve l'element detecte le mieux correspondant au texte cible."""
    scored = _score_candidates(elements, target, type_filter)
    if not scored:
        return None

    best, best_score = scored[0]

    # Good text similarity → accept immediately.
    if best_score > 0.25:
        return best

    # Pure type-only lookup (no target text specified → return first type match).
    if not target:
        return best

    # Target IS specified but every candidate scored ≤ 0.1 — meaning no element
    # had any readable text at all (OCR failed AND DOM enrichment was empty).
    # Accept the first type-matched candidate as a last resort, since we literally
    # have no text signal to do better.
    if bool(type_filter) and best is not None and best.type in type_filter:
        all_type_only = all(s <= 0.1 for _, s in scored)
        if all_type_only:
            return best  # last resort: accept first type-matched element

    return None


class ExecutorService:
    """Service d'execution des scripts de test Playwright."""

    def __init__(self):
        self._vision = None       # Lazy-loaded VisionService
        self._adaptation = None   # Lazy-loaded AdaptationService

    def _get_vision(self):
        """Charge VisionService une seule fois (evite les imports circulaires)."""
        if self._vision is None:
            from app.services.vision_service import VisionService  # noqa: PLC0415
            self._vision = VisionService()
        return self._vision

    def _get_adaptation(self):
        """Charge AdaptationService une seule fois."""
        if self._adaptation is None:
            from app.services.adaptation_service import AdaptationService  # noqa: PLC0415
            self._adaptation = AdaptationService()
        return self._adaptation

    @staticmethod
    def _compute_recovery_stats(results: list[StepResult]) -> RecoveryStats:
        """Calcule les statistiques de recuperation a partir des step results."""
        total = len(results)
        ok_first = sum(1 for r in results if r.statut == "OK" and r.retry_count == 0 and not r.visual_fallback_used)
        recovered = sum(1 for r in results if r.statut == "OK" and (r.retry_count > 0 or r.visual_fallback_used))
        failed = sum(1 for r in results if r.statut == "FAILED")
        visual_total = sum(1 for r in results if r.visual_fallback_used)
        adapt_total = sum(1 for r in results if r.adaptation_appliquee)
        denominator = recovered + failed
        taux = round(recovered / denominator, 3) if denominator > 0 else 1.0
        return RecoveryStats(
            steps_total=total,
            steps_ok=ok_first,
            steps_recovered=recovered,
            steps_failed=failed,
            taux_recuperation=taux,
            visual_fallback_total=visual_total,
            adaptation_total=adapt_total,
        )

    async def execute(self, request: TestExecutionRequest) -> TestExecutionResponse:
        """Execute un test et renvoie les resultats detailles."""
        test_id = uuid.uuid4().hex[:12]
        self._current_test_id = test_id
        self._current_step_idx = 0
        start = time.time()
        steps_results: list[StepResult] = []
        screenshots: list[str] = []

        os.makedirs(settings.SCREENSHOTS_DIR, exist_ok=True)

        # When headless=False (Watch Run), Playwright needs a display.
        # Prefer the host X11 display forwarded via the DISPLAY env var
        # (set in docker-compose.yml via X11 socket mount).  If no host
        # display is available, fall back to a local Xvfb virtual display.
        headless = request.headless if request.headless is not None else settings.HEADLESS
        xvfb_proc = None
        if not headless:
            if not os.environ.get("DISPLAY"):
                # No host display forwarded — try Xvfb as last resort.
                import subprocess, shutil
                if shutil.which("Xvfb"):
                    display = ":99"
                    xvfb_proc = subprocess.Popen(
                        ["Xvfb", display, "-screen", "0", "1280x720x24", "-ac"],
                        stdout=subprocess.DEVNULL,
                        stderr=subprocess.DEVNULL,
                    )
                    os.environ["DISPLAY"] = display
            # else: DISPLAY is already set from X11 forwarding — use it directly.

        try:
            result = await self._execute_internal(request, test_id, headless, steps_results, screenshots, start)
        finally:
            if xvfb_proc is not None:
                xvfb_proc.terminate()
                xvfb_proc.wait()
        return result

    async def _execute_internal(self, request, test_id, headless, steps_results, screenshots, start) -> TestExecutionResponse:
        async with async_playwright() as pw:
            browser = await pw.chromium.launch(
                headless=headless,
                slow_mo=settings.SLOW_MO_MS,
            )
            page = await browser.new_page()
            # Apply configured timeout so locator failures surface in STEP_TIMEOUT_MS
            # (not Playwright's 30s default). This is critical: without this, every
            # failed locator burns 30s before the visual DOM fallback can run.
            page.set_default_timeout(settings.STEP_TIMEOUT_MS)

            # Navigate to the target URL first
            if request.url_cible:
                step_start = time.time()
                try:
                    await page.goto(request.url_cible, timeout=15_000)
                    await page.wait_for_load_state(
                        "networkidle", timeout=settings.NETWORKIDLE_TIMEOUT_MS
                    )
                    # Day 13: auto-close cookie banners after navigation
                    await self._handle_cookie_popup(page)
                    steps_results.append(StepResult(
                        step=f"Navigate to {request.url_cible}",
                        statut="OK",
                        duree_ms=round((time.time() - step_start) * 1000, 1),
                    ))
                except Exception as e:
                    steps_results.append(StepResult(
                        step=f"Navigate to {request.url_cible}",
                        statut="FAILED",
                        duree_ms=round((time.time() - step_start) * 1000, 1),
                        erreur=str(e),
                    ))
                # Capture initial screenshot
                ss = await self._screenshot(page, test_id, "initial")
                if ss:
                    screenshots.append(ss)

            # Execute the generated script line by line
            if request.script_code:
                result, step_results_exec, ss_list = await self._execute_script(
                    page, request.script_code, test_id,
                    already_navigated_url=request.url_cible or "",
                )
                steps_results.extend(step_results_exec)
                screenshots.extend(ss_list)

            await browser.close()

        total_ms = (time.time() - start) * 1000
        any_failed = any(s.statut == "FAILED" for s in steps_results)
        recovery_stats = self._compute_recovery_stats(steps_results)

        return TestExecutionResponse(
            test_id=test_id,
            scenario_name=request.scenario_name,
            statut="FAILED" if any_failed else "PASSED",
            duree_ms=round(total_ms, 1),
            steps_results=steps_results,
            screenshots=screenshots,
            recovery_stats=recovery_stats,
        )

    async def execute_live(
        self,
        page: Page,
        steps: list[dict],
        test_id: str,
    ) -> tuple[list[StepResult], list[str]]:
        """Execute des steps sur une page deja ouverte (pour le pipeline)."""
        results: list[StepResult] = []
        screenshots: list[str] = []
        self._current_test_id = test_id
        self._current_step_idx = 0

        for i, step in enumerate(steps, 1):
            step_start = time.time()
            code_lines = step.get("lines", [])
            comment = step.get("comment", f"Step {i}")
            total_retries = 0
            total_visual = False
            total_adapt = ""
            step_trace: Optional[FallbackTrace] = None
            step_pw_err = ""

            try:
                for line in code_lines:
                    line = line.strip()
                    if not line or line.startswith("#"):
                        continue
                    rc, vu, ad, tr, pw = await self._exec_with_retry(page, line)
                    total_retries += rc
                    total_visual = total_visual or vu
                    total_adapt = total_adapt or ad
                    if tr is not None:
                        step_trace = tr
                    if pw:
                        step_pw_err = pw

                elapsed = round((time.time() - step_start) * 1000, 1)
                results.append(StepResult(
                    step=comment, statut="OK", duree_ms=elapsed,
                    retry_count=total_retries, visual_fallback_used=total_visual,
                    adaptation_appliquee=total_adapt,
                    fallback_trace=step_trace,
                    playwright_error=step_pw_err,
                ))
            except _StepFailedError as sfe:
                elapsed = round((time.time() - step_start) * 1000, 1)
                results.append(StepResult(
                    step=comment, statut="FAILED", duree_ms=elapsed,
                    erreur=str(sfe.original),
                    retry_count=sfe.retry_count,
                    visual_fallback_used=sfe.fallback_tried,
                    fallback_trace=sfe.fallback_trace or step_trace,
                    playwright_error=sfe.playwright_error or step_pw_err,
                ))
            except Exception as e:
                elapsed = round((time.time() - step_start) * 1000, 1)
                results.append(StepResult(
                    step=comment, statut="FAILED", duree_ms=elapsed, erreur=str(e)
                ))

            # Day 13: screenshot after EVERY step (configurable)
            if settings.STEP_SCREENSHOT:
                ss = await self._screenshot(page, test_id, f"step_{i}")
                if ss:
                    screenshots.append(ss)

        return results, screenshots

